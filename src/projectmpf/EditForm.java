    private void btnImagePathMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnImagePathMouseClicked
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(this);
        
        if(result == JFileChooser.APPROVE_OPTION)
        {
            File choosenFile =  fileChooser.getSelectedFile();
            txtImagePath.setText(choosenFile.getPath());
            
            extension = choosenFile.getPath().substring(choosenFile.getPath().lastIndexOf(".")+1,choosenFile.getPath().length());
            
            if(!extension.equals("jpg") && !extension.equals("png"))
            {
                checkWrongExtension = true;
            }
            else
            {
                checkWrongExtension = false;
                fileName = choosenFile.getName();    
            }
        }
        
    }//GEN-LAST:event_btnImagePathMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
         for(int i=0; i< UserList.imagePath.size(); i++)
        {
            if(!UserList.imagePath.elementAt(i).equals(""))
            {
                
                checkMaxItem = true;
            }
            else
            {
                checkMaxItem = false;
                break;
            }
        }
        if(txtItemName.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Item Name must be filled!");
        }
        else if(txtItemPrice.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Item Price must be filled!");
        }
        else if(!checkIsNumber(txtItemPrice.getText()))
        {
            JOptionPane.showMessageDialog(null, "Item Price must be a number!");
        }
        else if(checkWrongExtension || txtImagePath.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Item Image must be ‘jpg’ or ‘png’!");
        }
        else if(checkMaxItem)
        {
            JOptionPane.showMessageDialog(null, "Items List is full!");
        }
        else
        {
            for(int i=0; i< UserList.imagePath.size(); i++)
            {
                if(UserList.imagePath.elementAt(i).equals(""))
                {
                    UserList.itemName.setElementAt(txtItemName.getText(), i);
                    UserList.itemPrice.setElementAt(Integer.parseInt(txtItemPrice.getText()), i);
                        
                    File originalFile = new File(txtImagePath.getText());
                    File newFilePath = new File(System.getProperty("user.dir") + "\\Assets\\" + fileName);
                    
                    try {
                        Files.copy(originalFile.toPath(), newFilePath.toPath(), StandardCopyOption.REPLACE_EXISTING);
                    } catch (IOException ex) {
                        Logger.getLogger(EditForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    UserList.imagePath.setElementAt("Assets\\\\" + fileName, i);
                    reset();
                    break;
                }
            }
        } 
    }//GEN-LAST:event_btnAddMouseClicked

