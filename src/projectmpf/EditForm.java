    public void reset()
    {
        checkEmptyItem = false;
        checkWrongExtension = false; 
        checkMaxItem = false;
        fileName = null;
        extension = null;
        txtImagePath.setText("");
        txtItemName.setText("");
        txtItemPrice.setText("");
        
        try {
            for(int i=0; i < UserList.imagePath.size(); i++)
            {
                if(UserList.imagePath.elementAt(i).equals(""))
                {
                    continue;
                }
                else
                {
                    Image img = ImageIO.read(new File(UserList.imagePath.elementAt(i)));
                    ImageIcon icon = new ImageIcon(img.getScaledInstance(125, 125, Image.SCALE_DEFAULT));

                    switch (i) 
                    {
                        case 0:
                            lblImage1.setIcon(icon);
                            btnRemove1.setVisible(true);
                            revalidate();
                            break;
                        case 1:
                            lblImage2.setIcon(icon);
                            btnRemove2.setVisible(true);
                             revalidate();
                            break;
                        case 2:
                            lblImage3.setIcon(icon);
                            btnRemove3.setVisible(true);
                             revalidate();
                            break;
                        case 3:
                            lblImage4.setIcon(icon);
                            btnRemove4.setVisible(true);
                             revalidate();
                            break;
                        default:
                            break;
                    }
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public boolean checkIsNumber(String text)
    {
        try 
        {
            Integer.parseInt(text);
            return true;
        }
        catch (NumberFormatException e) {
             return false;
        }
    }
